export class GameDetails {
    constructor(id, title, thumbnail, description, genre, platform, status, gameUrl) {
        this.id = id;
        this.title = title;
        this.thumbnail = thumbnail;
        this.description = description;
        this.genre = genre;
        this.platform = platform;
        this.status = status;
        this.gameUrl = gameUrl;
    }
}
